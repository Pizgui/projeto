n = input('Digite algo: ')
print('O tipo primitivo deste valor é: ', type(n))
print('O valor contém apenas letras?', n.isalpha())
print('O valor contém apenas números?', n.isdigit())
print('O valor contém letras e/ou números?', n.isalnum())
print('O valor contém apenas dígitos minúsculos?', n.islower())
print('O valor contém apenas dígitos maiúsculos?', n.isupper())
print('O valor contém apenas espaços?', n.isspace())
print('O valor está em formato de título?', n.istitle())
print('O valor é numérico?', n.isnumeric())
print('O valor é decimal?', n.isdecimal())
print('O valor é imprimível?', n.isprintable())
print('O valor é um identificador válido?', n.isidentifier())
print('O valor pertence ao conjunto ASCII?', n.isascii())










